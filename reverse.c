#include "push_swap.h"

void	rra(char *a)
{
	int		i;
	int		tmp;
	size_t	max;

	i = 1;
	max = ft_strlen(a) - 1;
	tmp = a[max];
	while (i < max)
	{
		a[i + 1] = a[i];
	}
}
