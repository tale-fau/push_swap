#include "push_swap.h"

int	sorted_stack(t_stack *a)
{
	int	i;

	i = 0;
	while (i < a->size)
	{
		if (a->stack[i] + 1 < a->stack[i])
			return (1);
		i++;
	}
	return (0);
}