#include "libft.h"
#include <stdio.h>

int	ft_isdigit(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ((!(str[i] >= '0' && str[i] <= '9')) && (!(str[i] == '-'))
			&& (!(str[i] == '+')))
			return (1);
		i++;
	}
	return (0);
}
