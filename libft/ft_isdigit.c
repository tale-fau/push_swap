#include "libft.h"

int	ft_isdigit(char *str)
{
	int	i;

	i = -1;
	if (str[0] == '-' || str[0] == '+')
		i = 0;
	while (str[++i])
	{
		if ((!(str[i] >= '0' && str[i] <= '9')))
			return (1);
	}
	return (0);
}
